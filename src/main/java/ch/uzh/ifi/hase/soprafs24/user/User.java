package ch.uzh.ifi.hase.soprafs24.user;

import javax.persistence.*;

import ch.uzh.ifi.hase.soprafs24.game.entity.Player;

import java.io.Serializable;

/**
 * Internal User Representation
 * This class composes the internal representation of the user and defines how
 * the user is stored in the database.
 * Every variable will be mapped into a database field with the @Column
 * annotation
 * - nullable = false -> this cannot be left empty
 * - unique = true -> this value must be unqiue across the database -> composes
 * the primary key
 */
@Entity
@Table(name = "USER")
public class User implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue
    private Long id;

    @Column(nullable = false, unique = true)
    private String username;

    @Column(nullable = false, unique = true)
    private String token;

    @Column()
    private String password;

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    @Override
    public String getDisplayName() {
        return getUsername();
    }

    public void updateField(User userInput) {// update all the values which are updateable
        if (userInput.getUsername() != null) {
            setUsername(userInput.getUsername());
        }
        if (userInput.getPassword() != null) {
            setPassword(userInput.getPassword());
        }
    }
}